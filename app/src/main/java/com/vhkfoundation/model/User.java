package com.vhkfoundation.model;

public class User {
    private String ccode;
    private String city_name;
    private String created_at;
    private String decrypt_password;
    private String district_id;
    private String email;
    private Object email_verified_at;
    private int id;
    private String mobile;
    private String name;
    private String other_img;
    private String pincode;
    private String profile_img;
    private String role;
    private String state_id;
    private int status;
    private String trash;
    private String updated_at;
    private String user_type;
    private String verified;
    private int verified_by;

    // Getters and setters for all fields

    public String getCcode() { return ccode; }
    public void setCcode(String ccode) { this.ccode = ccode; }

    public String getCity_name() { return city_name; }
    public void setCity_name(String city_name) { this.city_name = city_name; }

    public String getCreated_at() { return created_at; }
    public void setCreated_at(String created_at) { this.created_at = created_at; }

    public String getDecrypt_password() { return decrypt_password; }
    public void setDecrypt_password(String decrypt_password) { this.decrypt_password = decrypt_password; }

    public String getDistrict_id() { return district_id; }
    public void setDistrict_id(String district_id) { this.district_id = district_id; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public Object getEmail_verified_at() { return email_verified_at; }
    public void setEmail_verified_at(Object email_verified_at) { this.email_verified_at = email_verified_at; }

    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public String getMobile() { return mobile; }
    public void setMobile(String mobile) { this.mobile = mobile; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getOther_img() { return other_img; }
    public void setOther_img(String other_img) { this.other_img = other_img; }

    public String getPincode() { return pincode; }
    public void setPincode(String pincode) { this.pincode = pincode; }

    public String getProfile_img() { return profile_img; }
    public void setProfile_img(String profile_img) { this.profile_img = profile_img; }

    public String getRole() { return role; }
    public void setRole(String role) { this.role = role; }

    public String getState_id() { return state_id; }
    public void setState_id(String state_id) { this.state_id = state_id; }

    public int getStatus() { return status; }
    public void setStatus(int status) { this.status = status; }

    public String getTrash() { return trash; }
    public void setTrash(String trash) { this.trash = trash; }

    public String getUpdated_at() { return updated_at; }
    public void setUpdated_at(String updated_at) { this.updated_at = updated_at; }

    public String getUser_type() { return user_type; }
    public void setUser_type(String user_type) { this.user_type = user_type; }

    public String getVerified() { return verified; }
    public void setVerified(String verified) { this.verified = verified; }

    public int getVerified_by() { return verified_by; }
    public void setVerified_by(int verified_by) { this.verified_by = verified_by; }
}

